void HammerDisplay::WriteText(String txt) {

    display.clearDisplay();
    display.setTextSize(1);
    display.setTextColor(WHITE);
    display.setCursor(0, 0);
    display.println(txt);
    display.display();

}

void HammerDisplay::WriteBool(bool v) {
    display.clearDisplay();
    display.setTextSize(1);
    display.setTextColor(WHITE);
    display.setCursor(0, 0);
    if (v) {

        display.println("true");
    } else {
        display.println("false");

    }
    display.display();
}

void HammerDisplay::WriteRotaryState(RotaryState state) {

    switch (state) {

        case Up:
            WriteText("up");
            break;
        case Down:
            WriteText("Down");
            break;
        case Nothing:
            WriteText("Nothing");
            break;
    }
}

void HammerDisplay::DrawFrame() {

    int index = 0;
    display.clearDisplay();
    for (int i = 0; i < 128; ++i) {
        for (int j = 0; j < 32; ++j) {
            Serial.println(index);
            display.drawPixel(j, i, frame[index]);
            index += 1;
        }
    }
    DrawKeys();
    DrawMotionBar();
    DrawProfileNames(2);
    display.display();

}

