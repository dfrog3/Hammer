void HammerDisplay::WriteText(String txt) {

    display.clearDisplay();
    display.setTextSize(1);
    display.setTextColor(WHITE);
    display.setCursor(0, 0);
    display.println(txt);
    display.display();

}

void HammerDisplay::WriteBool(bool v) {
    display.clearDisplay();
    display.setTextSize(1);
    display.setTextColor(WHITE);
    display.setCursor(0, 0);
    if (v) {

        display.println("true");
    } else {
        display.println("false");

    }
    display.display();
}

void HammerDisplay::WriteRotaryState(RotaryState state) {

    switch (state) {

        case Up:
            WriteText("up");
            break;
        case Down:
            WriteText("Down");
            break;
        case Nothing:
            WriteText("Nothing");
            break;
    }
}

void HammerDisplay::DrawFrame() {

    int index = 0;
    display.clearDisplay();
    for (int i = 0; i < 128; ++i) {
        for (int j = 0; j < 32; ++j) {
            display.drawPixel(j, i, frame[index]);
            index += 1;
        }
    }
    display.display();

}

void HammerDisplay::DrawKeys(HammerProfile *profile) {
    display.drawChar(11, 9, profile->getStrikeDisplay()[0], 1, 0, 2); // strike

    bool apple = HammerProfile::ContainsApple(profile->getStrikeSpecialKeys());
    bool shift = HammerProfile::ContainsShift(profile->getStrikeSpecialKeys());
    bool control = HammerProfile::ContainsControl(profile->getStrikeSpecialKeys());
    bool option = HammerProfile::ContainsOption(profile->getStrikeSpecialKeys());
    //swing specials
    display.drawChar(1, 32, 'A', !apple, apple, 1);
    display.drawChar(9, 32, 'S', !shift, shift, 1);
    display.drawChar(17, 32, 'C', !control, control, 1);
    display.drawChar(25, 32, 'O', !option, option, 1);

    display.drawChar(11, 43, profile->getThumbDisplay()[0], 1, 0, 2); // thumb

    apple = HammerProfile::ContainsApple(profile->getThumbSpecialKeys());
    shift = HammerProfile::ContainsShift(profile->getThumbSpecialKeys());
    control = HammerProfile::ContainsControl(profile->getThumbSpecialKeys());
    option = HammerProfile::ContainsOption(profile->getThumbSpecialKeys());
    //swing specials
    display.drawChar(1, 59, 'A', !apple, apple, 1);
    display.drawChar(9, 59, 'S', !shift, shift, 1);
    display.drawChar(17, 59, 'C', !control, control, 1);
    display.drawChar(25, 59, 'O', !option, option, 1);
    display.display();

}


void HammerDisplay::DrawMotionBar(float percentComplete, bool direction) {
    int firstHalf = direction;
    int secondHalf = !direction;
    int middlePoint = floorf(32 * percentComplete);

    if (!direction) {
        middlePoint = 32 - middlePoint;
    }
    display.drawLine(0, 70, middlePoint, 70, firstHalf);
    display.drawLine(0, 71, middlePoint, 71, firstHalf);
    display.drawLine(0, 72, middlePoint, 72, firstHalf);
    display.drawLine(0, 73, middlePoint, 73, firstHalf);

    display.drawLine(middlePoint, 70, 32, 70, secondHalf);
    display.drawLine(middlePoint, 71, 32, 71, secondHalf);
    display.drawLine(middlePoint, 72, 32, 72, secondHalf);
    display.drawLine(middlePoint, 73, 32, 73, secondHalf);
    display.display();

}

int Compare(int myMode, int setMode, bool defaultColor) {
    if (myMode == setMode) {
        return !defaultColor;
    } else {
        return defaultColor;
    }
}

