void HammerDisplay::Hide() {
    for (int i = 0; i < 128; ++i) {
        for (int j = 0; j < 32; ++j) {
            savedDisplay[j][i] = display.getPixel(j, i);
        }
    }


    display.clearDisplay();
    display.display();
}

void HammerDisplay::Show() {
    for (int i = 0; i < 128; ++i) {
        for (int j = 0; j < 32; ++j) {
            display.drawPixel(j, i, savedDisplay[j][i]);
        }
    }

    display.display();
}
