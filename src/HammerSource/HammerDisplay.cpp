void HammerDisplay::DrawMotionBar(float percentComplete) {
    int firstHalf = 1;
    int secondHalf = 0;
    int middlePoint = floorf(30 * percentComplete);

    display.drawLine(1, 70, middlePoint - 1, 70, firstHalf);
    display.drawLine(1, 71, middlePoint - 1, 71, firstHalf);
    display.drawLine(1, 72, middlePoint - 1, 72, firstHalf);
    display.drawLine(1, 73, middlePoint - 1, 73, firstHalf);

    display.drawLine(middlePoint, 70, 30, 70, secondHalf);
    display.drawLine(middlePoint, 71, 30, 71, secondHalf);
    display.drawLine(middlePoint, 72, 30, 72, secondHalf);
    display.drawLine(middlePoint, 73, 30, 73, secondHalf);

}

int Compare(int myMode, int setMode, bool defaultColor) {
    if (myMode == setMode) {
        return !defaultColor;
    } else {
        return defaultColor;
    }
}

